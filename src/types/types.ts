interface Position {
  x: number;
  y: number;
}

interface Size {
  width: number;
  height: number;
}

export interface Layer {
  type: string;
  name: string;
  src?: string;
  opacity?: number;
  position?: Position;
  size?: Size;
  zIndex?: number;
}

export interface GroupLayer extends Layer {
  type: "group";
  layers: Layer[];
}

export interface SingleLayer extends Layer {
  type: "single";
  layers: Layer[];
}
export type floorOrderForTypesType = string[];
export interface IConfig {
  floorOrder: floorOrderForTypesType;
  minZoom: number;
  maxZoom: number;
  layers: {
    [key: string]: GroupLayer | SingleLayer;
  };
}
interface DragData {
  startX: number;
  startY: number;
}
